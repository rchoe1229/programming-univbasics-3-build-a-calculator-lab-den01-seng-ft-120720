def addition(num1, num2)
  sum = num1 + num2
  puts sum
end

def subtraction(num1, num2)
  subtraction = num1 + num2
  puts subtraction
end

def division(num1, num2)
  div = num1 / num2
  puts div
end

def multiplication(num1, num2)
  mult = num1 * num2
  puts mult
end

def modulo(num1, num2)
  modulo = num1 % num2
  puts modulo
end

def square_root(num)
  Math.sqrt(num)
end